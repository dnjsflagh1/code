namespace MG
{
    //*****************************************************************************************************
    //GameObjectData
    struct GameObjectData
    {
        GameObjectIdType			mId                 ;        // 自己的ID号，唯一识别。
        Str16						mName               ;        // 自己的名字
        

        //------------------------------------------------------------------------
        GameObjectData()
            :mId(0)
			,mName(L"")
        {

        }
        //------------------------------------------------------------------------
        GameObjectData& operator=(const GameObjectData& A)
        {
			if (this == &A)
			{
				return *this;
			}

            mId                =    A.mId   ;
            mName              =    A.mName ;

            return *this;
        }

		//------------------------------------------------------------------------
		virtual void print()
		{
			MG_LOG(out_sys, "【NEW_GameObjectData】mID[%d],mName[%s]\n ",  mId, mName.c_str());
		}
    };

}
