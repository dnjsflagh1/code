	struct ItemData : public NoCharacterData
	{   
		U32 mItemTemplateId;
		U32 mItemNum;

		//------------------------------------------------------------------------------------------------
		ItemData()
			:mItemTemplateId(0)
			,mItemNum(0)
		{

		}
		//------------------------------------------------------------------------------------------------
		ItemData& operator=(const ItemData& A)
		{
			if (this == &A)
			{
				return *this;
			}

			NoCharacterData::operator =(A);

			mItemTemplateId = A.mItemTemplateId;
			mItemNum		= A.mItemNum;
			return *this;
		}

	};
