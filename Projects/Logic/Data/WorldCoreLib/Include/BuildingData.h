namespace MG
{

    /******************************************************************************/
    //战役建筑物基础属性
    /******************************************************************************/
    struct BuildingData : public CharacterData
    {   

        //------------------------------------------------------------------------------------------------
        BuildingData()
        {

        }
        //------------------------------------------------------------------------------------------------
        BuildingData& operator=(const BuildingData& A)
        {
            if (this == &A)
            {
                return *this;
            }

            CharacterData::operator =(A);

            return *this;
        }
    };
}
